class Unaryoperater
{
public static void main(String[]args)
{
int value1=20;
int value2=40;
System.out.println(value1);
System.out.println(value2);
System.out.println(++value1);
System.out.println(++value1);
System.out.println(value1--);
System.out.println(value1--);
System.out.println(++value2);
System.out.println(++value2);
System.out.println(value2--);
System.out.println(value2--);
}
}
